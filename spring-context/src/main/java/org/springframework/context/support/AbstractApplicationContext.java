	/**
	 * Instantiate and invoke all registered BeanFactoryPostProcessor beans,
	 * respecting explicit order if given.
	 * <p>Must be called before singleton instantiation.
	 */
	protected void invokeBeanFactoryPostProcessors(ConfigurableListableBeanFactory beanFactory) {
		/**
		 * AbstractApplicationContext的
		 * private final List<BeanFactoryPostProcessor> beanFactoryPostProcessors = new ArrayList<>();
		 * 一个比较核心的代理类出现了，AbstractApplicationContext委托执行post processors任务的工具类
		 *
		 * =============
		 * spring-boot
		 * SpringApplication类中applyInitializers(context);它会将三个默认的内部类加入到 spring 容器DefaultListableBeanFactory中
		 * ConfigurationWarningsApplicationContextInitializer$ConfigurationWarningsPostProcessor
		 * SharedMetadataReaderFactoryContextInitializer$CachingMetadataReaderFactoryPostProcessor
		 * ConfigFileApplicationListener$PropertySourceOrderingPostProcessor
		 */
		PostProcessorRegistrationDelegate.invokeBeanFactoryPostProcessors(beanFactory, getBeanFactoryPostProcessors());

		// Detect a LoadTimeWeaver and prepare for weaving, if found in the meantime
		// (e.g. through an @Bean method registered by ConfigurationClassPostProcessor)

		/**
		 * 在Java 语言中，从织入切面的方式上来看，存在三种织入方式：
		 * 编译期织入、类加载期织入和运行期织入。编译期织入是指在Java编译期，
		 * 采用特殊的编译器，将切面织入到Java类中；而类加载期织入则指通过特殊
		 * 的类加载器，在类字节码加载到JVM时，织入切面；运行期织入则是采用
		 * CGLib工具或JDK动态代理进行切面的织入。
		 * AspectJ采用编译期织入和类加载期织入的方式织入切面，
		 * 是语言级的AOP实现，提供了完备的AOP支持。它用AspectJ语
		 * 言定义切面，在编译期或类加载期将切面织入到Java类中。
		 * AspectJ提供了两种切面织入方式，第一种通过特殊编译器，
		 * 在编译期，将AspectJ语言编写的切面类织入到Java类中，
		 * 可以通过一个Ant或Maven任务来完成这个操作；第二种方式是
		 * 类加载期织入，也简称为LTW（Load Time Weaving）
		 */
		if (beanFactory.getTempClassLoader() == null && beanFactory.containsBean(LOAD_TIME_WEAVER_BEAN_NAME)) {
			beanFactory.addBeanPostProcessor(new LoadTimeWeaverAwareProcessor(beanFactory));
			beanFactory.setTempClassLoader(new ContextTypeMatchClassLoader(beanFactory.getBeanClassLoader()));
		}
	}

